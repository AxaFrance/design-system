import { maskFrenchPhoneNumber } from "../maskFrenchPhoneNumber";

const cases = [
  ["0123456789", "01 23 45 67 89"],
  ["01-23.45 67 89", "01 23 45 67 89"],
  ["01234567890123456789", "01 23 45 67 89"],
  ["", ""],
  ["012345", "01 23 45"],
  ["0123456789", "01 23 45 67 89"],
  ["01234567890123456789", "01 23 45 67 89"],
  ["01 23 45 67 89", "01 23 45 67 89"],
  ["01-23-45-67-89", "01 23 45 67 89"],
  ["01a23b45c67d89", "01 23 45 67 89"],
  ["0001234567", "00 01 23 45 67"],
  ["0123456789   ", "01 23 45 67 89"],
  ["   0123456789", "01 23 45 67 89"],
  ["01-23 45.67 89", "01 23 45 67 89"],
  ["01@23#45$67%89", "01 23 45 67 89"],
];

describe.each(cases)("maskFrenchPhoneNumber('%s')", (input, expectedOutput) => {
  it(`devrait retourner "${expectedOutput}"`, () => {
    expect(maskFrenchPhoneNumber(input)).toBe(expectedOutput);
  });
});
